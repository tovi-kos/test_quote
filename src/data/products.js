// Helper function to get total for all products
export const getTotalForNetwork = () => {
  return data.productCategories.reduce((total, category) => {
    const categoryTotal = category.products.reduce((sum, product) => sum + product.total, 0);
    return total + categoryTotal;
  }, 0);
};

// Helper function to get products by category name
export const getProductsByCategory = (categoryName) => {
  const category = data.productCategories.find(cat => cat.categoryName === categoryName);
  return category ? category.products : [];
};

// Helper function to get unique category names
export const getUniqueCategories = () => {
  return data.productCategories.map(category => category.categoryName);
};

// Helper function to get total for a specific category
export const getCategoryTotal = (categoryName) => {
  const category = data.productCategories.find(cat => cat.categoryName === categoryName);
  if (!category) return 0;
  return category.products.reduce((sum, product) => sum + product.total, 0);
};

// Helper function to get all labor data from categories
export const getLaborData = () => {
  return data.productCategories.map(category => ({
    categoryName: category.categoryName,
    description: category.labor.descriptionLabor,
    price: category.labor.price
  }));
};

// Helper function to get total labor cost
export const getTotalLabor = () => {
  return data.productCategories.reduce((total, category) => {
    return total + category.labor.price;
  }, 0);
};